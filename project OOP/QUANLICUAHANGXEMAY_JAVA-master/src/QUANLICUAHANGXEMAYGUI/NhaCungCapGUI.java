    private void btnxoaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnxoaActionPerformed
        // TODO add your handling code here:
        NhaCungCap nv = new NhaCungCap();
        if (row_table >= 0) {
            nv.setMaNhaCungCap(tblNhaCungCap.getValueAt(row_table, 0).toString());
            int dialogResult = JOptionPane.showConfirmDialog(this, "Bạn muốn xóa bản ghi?", "Cảnh báo", JOptionPane.YES_NO_OPTION);
            if (dialogResult == JOptionPane.YES_OPTION) {
                if (NhaCungCapdll.deleteData(nv)) {
                    demrow();
                      if(row_table==0)
                        trang=soTrang;
                     else 
                   trang=Integer.parseInt(lbltrang.getText());
                    demrow();
                    Biding();
                    JOptionPane.showMessageDialog(this, "Xóa thành công!");
                } else {
                    JOptionPane.showMessageDialog(this, "Xóa không thành công!");
                }
            }
        } else {
            JOptionPane.showMessageDialog(this, "Chọn bạn ghi cần xóa trước");
        }
    }//GEN-LAST:event_btnxoaActionPerformed
    void timkiem() {
        Vector column = new Vector();
        column.add("Mã nhà cung cấp");
        column.add("Tên nhà cung cấp");
        column.add("Địa chỉ");
        column.add("Điện thoai");
        column.add("Email");

        Vector data = new Vector();
        //dbo.fuChuyenCoDauThanhKhongDau(TenNhaCungCap) like '%'+"+txtsearch.getText()+"+'%'
        //  
        listNhaCungCap = NhaCungCapdll.getALL("", "dbo.fuChuyenCoDauThanhKhongDau(TenNhaCC) like N'%'+dbo.fuChuyenCoDauThanhKhongDau(N'" + txtsearch.getText() + "')+'%'", "");
        for (NhaCungCap kh : listNhaCungCap) {
            Vector row = new Vector();
            row.add(kh.getMaNhaCungCap());
            row.add(kh.getTenNhaCungCap());
            row.add(kh.getDiaChi());
            row.add(kh.getDienThoai());
            row.add(kh.getEmail());
            data.add(row);
        }
        DefaultTableModel dtm = new DefaultTableModel(data, column);
        tblNhaCungCap.setModel(dtm);
    }
