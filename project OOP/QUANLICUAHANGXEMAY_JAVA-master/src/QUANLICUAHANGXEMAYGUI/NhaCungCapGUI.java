    public void Biding() {
        Vector column = new Vector();
        column.add("Mã nhà cung cấp");
        column.add("Tên nhà cung cấp");
        column.add("Địa chỉ");
        column.add("Điện thoại");
        column.add("Email");

        Vector data = new Vector();
        listNhaCungCap = NhaCungCapdll.getALL(" 5 ", " [MaNhaCC] not in (SELECT top " + (trang * 5 - 5) + " [MaNhaCC] from dbo.NhaCungCap )", " MaNhaCC ASC");
        for (NhaCungCap kh : listNhaCungCap) {
            Vector row = new Vector();
            row.add(kh.getMaNhaCungCap());
            row.add(kh.getTenNhaCungCap());
            row.add(kh.getDiaChi());
            row.add(kh.getDienThoai());
            row.add(kh.getEmail());
            data.add(row);
        }
        DefaultTableModel dtm = new DefaultTableModel(data, column);
        tblNhaCungCap.setModel(dtm);
    }
