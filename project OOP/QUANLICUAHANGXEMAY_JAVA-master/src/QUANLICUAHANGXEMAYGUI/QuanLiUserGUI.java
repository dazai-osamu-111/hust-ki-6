     void timkiem()
    {
       
         Vector column = new Vector();
        column.add("Tài khoản");
        column.add("Mã nhân viên");
        column.add("Mật khẩu ");
        column.add("Quyền ");
        //TenKH like N'%" + txtSearchKH.getText() + "%'
//dbo.fuChuyenCoDauThanhKhongDau(TenNhanVien) like N'%'+dbo.fuChuyenCoDauThanhKhongDau('"+txtSearch.getText()+"')+'%'"
        listKH = KHdll.getALL("", "dbo.fuChuyenCoDauThanhKhongDau([User]) like N'%'+dbo.fuChuyenCoDauThanhKhongDau('"+txtSearch.getText()+"')+'%'", "");
           Vector data = new Vector();
      
        for (User kh : listKH) {
            Vector row = new Vector();
            row.add(kh.getUser());
            row.add(kh.getMaNhanVien());
           row.add(kh.getMatKhauMoi());
           row.add(kh.getQuyen());
            data.add(row);
        }
        DefaultTableModel dtm = new DefaultTableModel(data, column);
        tblUser.setModel(dtm);
    }
