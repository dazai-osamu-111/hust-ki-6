    private void btnluuActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnluuActionPerformed
        // TODO add your handling code here:
         if (them == true) {
          
            if(txtuser.getText().equals("")==true || txtmatkhau.getText().equals("")==true||cbbmanhanvien.getSelectedItem()==null)
            {
                 
                JOptionPane.showMessageDialog(this, "Bạn bắt buộc phải nhập User và mật khẩu!!! ");
                return;
                
            
            }
               else
            {
                
                ArrayList dong=KHdll.CheckID(txtuser.getText());
                if(dong.size()!=0)
                {
                    JOptionPane.showMessageDialog(this,"Mã nhân viên đã bị trùng mời bạn nhâp lại!!!");
                    txtuser.requestFocus();
                    txtuser.selectAll();
                    return;
                }
                else
                {
                    try {
                        User nv = new User();
                        nv.setMaNhanVien(cbbmanhanvien.getSelectedItem().toString());
                        nv.setMatKhauMoi(txtmatkhau.getText());
                        nv.setUser(txtuser.getText());
                        nv.setQuyen(cbbquyen.getSelectedItem().toString());
                        KHdll.AddData(nv);
                        Biding();
                         
                    } catch (Exception e) {
                        
                    }
                    
                    JOptionPane.showMessageDialog(this, "Thêm thành công!!!");
                    
                }
            }
          
           
            
        }
        else
        {
              try {
                User nv = new User();
                        nv.setMaNhanVien(cbbmanhanvien.getSelectedItem().toString());
                        nv.setMatKhauMoi(txtmatkhau.getText());
                        nv.setUser(txtuser.getText());
                        nv.setQuyen(cbbquyen.getSelectedItem().toString());
                      
                       
               if( KHdll.updateData(nv)==true)
               {
                   JOptionPane.showMessageDialog(this, "Cập nhật thành công!!!");
               }
               else
               {
                    JOptionPane.showMessageDialog(this, "Bạn chưa chọn điều khiển!!!","Thông báo",JOptionPane.ERROR_MESSAGE);
                    return;
               }
                Biding();
                

            } catch (Exception e) {

            }
            
        
        }
        LamMoi();
    }//GEN-LAST:event_btnluuActionPerformed

    private void btnsuaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnsuaActionPerformed
        // TODO add your handling code here:
         if (row_table <0) {
            JOptionPane.showMessageDialog(this, "Bạn chưa nhập dòng của bảng muốn sửa!!!");
            return;
        } else {
            txtuser.setEnabled(false);
            them = false;
            btnthem.setEnabled(false);
            listKH = KHdll.getALL("", "", "");
            txtuser.setText(listKH.get(row_table).getUser());
            txtmatkhau.setText(listKH.get(row_table).getMatKhauMoi());
            DefaultComboBoxModel model = new DefaultComboBoxModel();
            model.addElement(listKH.get(row_table).getMaNhanVien().toString().trim());
            model.setSelectedItem(model.getElementAt(0));
            cbbmanhanvien.setModel(model);
          
         
             cbbquyen.setSelectedItem(listKH.get(row_table).getQuyen().toString().trim());
            
           

        }
    }//GEN-LAST:event_btnsuaActionPerformed

