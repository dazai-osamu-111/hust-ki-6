    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        // TODO add your handling code here:

        if (row_table == -1) {
            JOptionPane.showMessageDialog(null, "Bạn phải chọn dòng !!", "Thông báo", JOptionPane.INFORMATION_MESSAGE);
            return;
        } else {
            //            PhieuNhapGUI.cbbmanhanhacungcap.setEditable(true);
            //            // PhieuNhapGUI.cbbmanhanhacungcap.getEditor().setItem(listNhaCungCap.get(row_table).getMaNhaCungCap().trim());
            //            PhieuNhapGUI.cbbmanhanhacungcap.setSelectedItem(listNhaCungCap.get(row_table).getMaNhaCungCap().toString().trim());
            DefaultComboBoxModel model = new DefaultComboBoxModel();
            model.addElement(listXe.get(row_table).getMaXe().toString().trim());
            model.setSelectedItem(model.getElementAt(0));
            if (PhieuNhapGUI.value == 0) {
                PhieuNhapGUI.cbbmaxe.setModel(model);
                PhieuNhapGUI.txtdongia.setText(listXe.get(row_table).getDonGia().toString());
            }
            if (PhieuXuatGUI.value == 1) {
                PhieuXuatGUI.txtdongia.setText(listXe.get(row_table).getDonGia().toString());
                PhieuXuatGUI.cbbmaxe.setModel(model);
            }

            this.hide();
        }

    }//GEN-LAST:event_jButton2ActionPerformed

