    public void Biding() {
        Vector column = new Vector();
        column.add("Mã Xe");
        column.add("Tên hãng xe");
        column.add("Số lượng");
        column.add("Thông tin bảo hành");
        column.add("Tên Xe");

        column.add("Nhà Sản Xuất");
        column.add("Đơn giá");
        column.add("Tình trạng");

        Vector data = new Vector();
        listXe = Xedll.getALL(" 5 ", " [MaXe] not in (SELECT top "+(trang*5-5) +" [MaXe] from dbo.Xe )", "");
        for (Xe kh : listXe) {
            Vector row = new Vector();
            row.add(kh.getMaXe());
            row.add(kh.getTenHangXe());
            row.add(kh.getSoLuong());
            row.add(kh.getThongTinBaoHanh());
            row.add(kh.getTenXe());
            row.add(kh.getNhaSX());
            row.add(kh.getDonGia());
            row.add(kh.getTinhTrang());

            data.add(row);
        }
        DefaultTableModel dtm = new DefaultTableModel(data, column);
        tblXe.setModel(dtm);
    }

