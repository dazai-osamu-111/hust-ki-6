    void timkiem() {
        Vector column = new Vector();
        column.add("Mã nhân viên");
        column.add("Tên nhân viên");
        column.add("Ngày sinh");
        column.add("Giới tính");
        column.add("Địa chỉ");
        column.add("Điện thoại");
        column.add("Chức vụ");
        column.add("Lương cơ bản");
        column.add("Ngày vào làm");
        column.add("Lương");

        Vector data = new Vector();
        listNV = NVdll.getALL("", " dbo.fuChuyenCoDauThanhKhongDau(TenNhanVien) like N'%'+dbo.fuChuyenCoDauThanhKhongDau(N'" + txtSearch.getText() + "')+'%'", "");
        for (NhanVien nv : listNV) {
            Vector row = new Vector();
            row.add(nv.getMaNhanVien());
            row.add(nv.getTenNhanVien());
            row.add(nv.getNgaySinh());
            row.add(nv.getGioiTinh());
            row.add(nv.getDiaChi());
            row.add(nv.getDienThoai());
            row.add(nv.getChucvu());
            row.add(nv.getLuongCoBan());
            row.add(nv.getNgayvaolam());
            row.add(nv.getLuong());
            data.add(row);
        }
        DefaultTableModel dtm = new DefaultTableModel(data, column);
        tblNhanVien.setModel(dtm);
    }
