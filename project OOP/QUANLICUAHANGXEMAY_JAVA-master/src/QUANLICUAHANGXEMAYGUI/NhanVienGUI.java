    private void btnxoaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnxoaActionPerformed
        // TODO add your handling code here:
        // TODO add your handling code here:
        NhanVien nv = new NhanVien();
        if (row_table >= 0) {
            nv.setMaNhanVien(tblNhanVien.getValueAt(row_table, 0).toString());
            int dialogResult = JOptionPane.showConfirmDialog(this, "Bạn muốn xóa bản ghi?", "Cảnh báo", JOptionPane.YES_NO_OPTION);
            if (dialogResult == JOptionPane.YES_OPTION) {
                if (NVdll.deleteData(nv)) {
                    demrow();
                    if (row_table == 0) {
                        trang = soTrang;
                    } else {
                        trang = Integer.parseInt(lbltrang.getText());
                    }
                    demrow();
                    Biding();
                    JOptionPane.showMessageDialog(this, "Xóa thành công!");
                } else {
                    JOptionPane.showMessageDialog(this, "Xóa không thành công!");
                }
            }
        } else {
            JOptionPane.showMessageDialog(this, "Chọn bạn ghi cần xóa trước");
        }
    }//GEN-LAST:event_btnxoaActionPerformed

    private void btnluuActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnluuActionPerformed
        // TODO add your handling code here:

        if (them == true) {

            if (txtmanv.getText().equals("") == false || txttennv.getText().equals("") == false) {
                ArrayList dong = NVdll.CheckID(txtmanv.getText());
                if (dong.size() != 0) {
                    JOptionPane.showMessageDialog(this, "Mã nhân viên đã bị trùng mời bạn nhâp lại!!!");
                    return;
                } else {
                    try {

                        NhanVien nv = new NhanVien();
                        nv.setMaNhanVien(txtmanv.getText());
                        nv.setTenNhanVien(txttennv.getText());

//                        DateFormat formatter=new SimpleDateFormat("yyyy/MM/dd");
//                       
//                        
//                        nv.setNgaySinh(formatter.format(datechoosengaysinh.getDate()));
//                        nv.setNgayvaolam(formatter.format(datechoosengayvl.getDate()));
                        nv.setNgaySinh(DateFormat.getDateInstance().format(datechoosengaysinh.getDate()));
                        nv.setNgayvaolam(DateFormat.getDateInstance().format(datechoosengayvl.getDate()));
                        if (rdbtnnam.isSelected()) {
                            nv.setGioiTinh("Nam");
                        } else {
                            nv.setGioiTinh("Nữ");
                        }

                        nv.setDiaChi(txtdc.getText());
                        nv.setDienThoai(txtdienthoai.getText());
                        nv.setLuong(txtluong.getText());
                        nv.setLuongCoBan(txtluongcoban.getText());
                        nv.setChucvu(cbbchucvu.getSelectedItem().toString());
                        NVdll.AddData(nv);
                        demrow();
                        trang = soTrang;
                        demrow();
                        Biding();
                    } catch (Exception e) {

                    }

                    JOptionPane.showMessageDialog(this, "Thêm thành công!!!");

                }
            } else {
                JOptionPane.showMessageDialog(this, "Bạn bắt buộc phải nhập mã nhân viên và tên nhân viên");
                return;
            }

        } else {
            try {
                NhanVien nv = new NhanVien();
                nv.setMaNhanVien(txtmanv.getText());
                nv.setTenNhanVien(txttennv.getText());
                nv.setNgaySinh(DateFormat.getDateInstance().format(datechoosengaysinh.getDate()));
                if (rdbtnnam.isSelected()) {
                    nv.setGioiTinh("Nam");
                } else {
                    nv.setGioiTinh("Nữ");
                }
                nv.setNgayvaolam(DateFormat.getDateInstance().format(datechoosengayvl.getDate()));
                nv.setDiaChi(txtdc.getText());
                nv.setDienThoai(txtdienthoai.getText());
                nv.setLuong(txtluong.getText());
                nv.setLuongCoBan(txtluongcoban.getText());
                nv.setChucvu(cbbchucvu.getSelectedItem().toString());
                if (NVdll.updateData(nv) == true) {
                    JOptionPane.showMessageDialog(this, "Cập nhật thành công!!!");
                } else {
                    JOptionPane.showMessageDialog(this, "Bạn chưa chọn điều khiển!!!", "Thông báo", JOptionPane.ERROR_MESSAGE);
                    return;
                }
                Biding();

            } catch (Exception e) {

            }

        }
        LamMoi();


    }//GEN-LAST:event_btnluuActionPerformed

    private void btnsuaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnsuaActionPerformed
        // TODO add your handling code here:
        if (row_table < 0) {
            JOptionPane.showMessageDialog(this, "Bạn chưa nhập dòng của bảng muốn sửa!!!");
            return;
        } else {
            txtmanv.setEnabled(false);
            them = false;
            btnthem.setEnabled(false);
            btnxoa.setEnabled(false);
            // listNV = NVdll.getALL("", "", "");
            txtmanv.setText(listNV.get(row_table).getMaNhanVien());
            txttennv.setText(listNV.get(row_table).getTenNhanVien());
            txtdc.setText(listNV.get(row_table).getDiaChi());
            txtdienthoai.setText(listNV.get(row_table).getDienThoai());
            DateFormat formatter = new SimpleDateFormat("dd/MM/yyyy");
            try {

                Date date = formatter.parse(listNV.get(row_table).getNgaySinh());
                datechoosengaysinh.setDate(date);
            } catch (ParseException ex) {
                Logger.getLogger(NhanVienGUI.class.getName()).log(Level.SEVERE, null, ex);
            }
            try {

                Date date = formatter.parse(listNV.get(row_table).getNgayvaolam());
                datechoosengayvl.setDate(date);
            } catch (ParseException ex) {
                Logger.getLogger(NhanVienGUI.class.getName()).log(Level.SEVERE, null, ex);

            }
            //DefaultTableModel model2 = (DefaultTableModel) tblNhanVien.getModel();
            //   cboLoai.setSelectedItem(tbl.getValueAt(r, 4).toString().trim());

            String s = tblNhanVien.getValueAt(row_table, 6).toString().trim();
            cbbchucvu.setSelectedItem(s);
//            if(s.toLowerCase().equals(("Trưởng phòng").toLowerCase())==true)
//            cbbchucvu.setSelectedIndex(1);
            txtluong.setText(listNV.get(row_table).getLuong());
            txtluongcoban.setText(listNV.get(row_table).getLuongCoBan());
            if (listNV.get(row_table).getGioiTinh().equals("Nam")) {
                rdbtnnam.setSelected(true);
            } else {
                rdbtnnu.setSelected(true);
            }
    }//GEN-LAST:event_btnsuaActionPerformed
    }
