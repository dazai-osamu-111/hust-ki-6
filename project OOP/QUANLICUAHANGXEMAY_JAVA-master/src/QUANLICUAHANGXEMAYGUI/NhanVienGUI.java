    private void cbbchucvuActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_cbbchucvuActionPerformed
        // TODO add your handling code here:
        int ngayvaolam, luong;
        switch (cbbchucvu.getSelectedIndex()) {
            case 0: {
                txtluongcoban.setText("5000000");
                SimpleDateFormat dateFormat = new SimpleDateFormat("yyyy");
                ngayvaolam = Integer.parseInt(dateFormat.format(datechoosengayvl.getDate()));
                luong = (ngayvaolam - 2010) * 500000 + 5000000;
                txtluong.setText(Integer.toString(luong));
                break;
            }
            case 1: {
                txtluongcoban.setText("4000000");
                SimpleDateFormat dateFormat = new SimpleDateFormat("yyyy");

                ngayvaolam = Integer.parseInt(dateFormat.format(datechoosengayvl.getDate()));
                luong = (ngayvaolam - 2010) * 300000 + 4000000;
                txtluong.setText(Integer.toString(luong));
                break;
            }
            default: {
                txtluongcoban.setText("3000000");
                SimpleDateFormat dateFormat = new SimpleDateFormat("yyyy");
                ngayvaolam = Integer.parseInt(dateFormat.format(datechoosengayvl.getDate()));
                luong = (ngayvaolam - 2010) * 200000 + 3000000;
                txtluong.setText(Integer.toString(luong));
                break;
            }
        }
    }//GEN-LAST:event_cbbchucvuActionPerformed

