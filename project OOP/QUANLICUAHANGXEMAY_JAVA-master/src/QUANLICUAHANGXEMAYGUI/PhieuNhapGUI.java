    private void jButton20ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton20ActionPerformed
        // TODO add your handling code here:
        them_PhieuNhap = false;
        txtmaphieunhap.setEnabled(false);
        if (row_table_PhieuNhap < 0) {
            JOptionPane.showMessageDialog(this, "Bạn chưa nhập dòng của bảng muốn sửa!!!");
            return;
        } else {
            txtmaphieunhap.setText(listPhieuNhap.get(row_table_PhieuNhap).getMaPhieuNhap());
            DateFormat formatter = new SimpleDateFormat("dd/MM/yyyy");
            try {

                Date date = formatter.parse(listPhieuNhap.get(row_table_PhieuNhap).getNgayNhap());
                datechooserngaynhapnam.setDate(date);
            } catch (ParseException ex) {
                Logger.getLogger(NhanVienGUI.class.getName()).log(Level.SEVERE, null, ex);
            }
            DefaultComboBoxModel model = new DefaultComboBoxModel();
            model.addElement(listPhieuNhap.get(row_table_PhieuNhap).getMaNhaCungCap().toString().trim());
            model.setSelectedItem(model.getElementAt(0));
            PhieuNhapGUI.cbbmanhanhacungcap.setModel(model);
            txtmaphieunhap.setEnabled(false);

        }
    }//GEN-LAST:event_jButton20ActionPerformed

