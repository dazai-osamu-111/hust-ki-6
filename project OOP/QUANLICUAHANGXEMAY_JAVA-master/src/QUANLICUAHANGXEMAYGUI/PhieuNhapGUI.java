    private void jButton19ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton19ActionPerformed
        // TODO add your handling code here:
        if (them_PhieuNhap == true) {

            if (txtmaphieunhap.getText().equals("") == true) {
                JOptionPane.showMessageDialog(this, "Bạn phải nhập đầy đủ thông tin!!!");
                return;
            } else {
                ArrayList dong = PhieuNhapdll.CheckID(txtmaphieunhap.getText());
                if (dong.size() != 0) {
                    JOptionPane.showMessageDialog(this, "Mã phiếu nhập đã bị trùng mời bạn nhâp lại!!!");
                    return;
                } else {
                    try {
                        PhieuNhap nv = new PhieuNhap();
                        nv.setMaNhanVien(txtmanhanvien.getText());
                        nv.setNgayNhap(DateFormat.getDateInstance().format(datechooserngaynhapnam.getDate()));
                        nv.setMaNhaCungCap(cbbmanhanhacungcap.getSelectedItem().toString());
                        nv.setMaPhieuNhap(txtmaphieunhap.getText());
                        if (PhieuNhapdll.AddData(nv) == true) {
                            JOptionPane.showMessageDialog(this, "Thêm thành công!!!");

                        } else {
                            JOptionPane.showMessageDialog(this, "Bạn đã nhập trùng mã phiếu nhập!!! ");
                            return;
                        }
                    } catch (Exception e) {

                    }

                }
            }

        } else {
            try {
                PhieuNhap nv = new PhieuNhap();
                nv.setMaNhanVien(txtmanhanvien.getText());
                nv.setNgayNhap(DateFormat.getDateInstance().format(datechooserngaynhapnam.getDate()));
                nv.setMaNhaCungCap(cbbmanhanhacungcap.getSelectedItem().toString());
                nv.setMaPhieuNhap(txtmaphieunhap.getText());
                PhieuNhapdll.updateData(nv);
                Biding_PhieuNhap();
                JOptionPane.showMessageDialog(this, "Sửa thành công!!!");
            } catch (Exception e) {

            }
        }
        Biding_PhieuNhap();
        LamMoi_tPhieuNhap();


    }//GEN-LAST:event_jButton19ActionPerformed
