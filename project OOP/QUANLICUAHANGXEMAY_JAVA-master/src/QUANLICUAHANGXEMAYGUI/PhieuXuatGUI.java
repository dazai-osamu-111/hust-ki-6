    private void btnluuActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnluuActionPerformed
        // TODO add your handling code here:
        if (them_CTPN == true) {
            if (Integer.parseInt(spnsoluong.getValue().toString()) == 0 ||cbbmaxe.getSelectedItem()==null) {
                JOptionPane.showMessageDialog(this, "Bạn phải nhập đầy đủ thông tin!!!");
                return;
            }
            if (Check_SoLuong() == false) {
                JOptionPane.showMessageDialog(this, "Số lượng hàng không đủ!!!");
                return;
            }
             else {

                ArrayList dong = CTPNdll.CheckID(txtmahdctpx.getText(), cbbmaxe.getSelectedItem().toString());
                if (dong.size() != 0) {
                    JOptionPane.showMessageDialog(this, "Mã phiếu nhập đã bị trùng mời bạn nhâp lại!!!");
                    return;
                } else {
                    try {
                        ChiTietPhieuXuat nv = new ChiTietPhieuXuat();
                        nv.setMaPhieuXuat(txtmahdctpx.getText());
                        nv.setMaXe(cbbmaxe.getSelectedItem().toString());
                        nv.setSLXuat(spnsoluong.getValue().toString());
                        nv.setDonGiaXuat(txtdongia.getText());
                        nv.setThue(txtthue.getText());
                        if (CTPNdll.AddData(nv) == true) {
                            JOptionPane.showMessageDialog(this, "Thêm thành công!!!");
                        } else {
                            JOptionPane.showMessageDialog(this, "Bạn đã nhập trùng Mã Phiếu Nhập");
                        }

                    } catch (Exception e) {

                    }

                }
            }

        } else if (Check_SoLuong() == false) {
            JOptionPane.showMessageDialog(this, "Số lượng hàng không đủ!!!");
            return;
        } else {
            try {
                ChiTietPhieuXuat nv = new ChiTietPhieuXuat();
                nv.setMaPhieuXuat(txtmahdctpx.getText());
                nv.setMaXe(cbbmaxe.getSelectedItem().toString());
                nv.setSLXuat(spnsoluong.getValue().toString());
                nv.setDonGiaXuat(txtdongia.getText());
                nv.setThue(txtthue.getText());

                if (CTPNdll.updateData(nv) == true) {
                    JOptionPane.showMessageDialog(this, "Cập nhật thành công!!!");
                } else {
                    JOptionPane.showMessageDialog(this, "Bạn chưa chọn điều khiển!!!", "Thông báo", JOptionPane.ERROR_MESSAGE);
                }
                Biding_ChiTietPhieuXuat();

            } catch (Exception e) {

            }

        }
        LamMoi_ChiTietPhieuXuat();
        timkiem_ChiTietPhieuXuat();
    }//GEN-LAST:event_btnluuActionPerformed

