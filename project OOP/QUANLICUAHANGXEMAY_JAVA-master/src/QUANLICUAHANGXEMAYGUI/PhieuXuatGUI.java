    private void jButton19ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton19ActionPerformed
        // TODO add your handling code here:
        if (them_PhieuXuat == true) {

            if (txtmahdpx.getText().equals("") == true) {
                JOptionPane.showMessageDialog(this, "Bạn phải nhập đầy đủ thông tin!!!");
                return;
            } else {
                ArrayList dong = PhieuXuatdll.CheckID(txtmahdpx.getText());
                if (dong.size() != 0) {
                    JOptionPane.showMessageDialog(this, "Mã phiếu nhập đã bị trùng mời bạn nhâp lại!!!");
                    return;
                } else {
                    try {
                        PhieuXuat nv = new PhieuXuat();
                        nv.setMaNhanVien(txtmanhanvien.getText());
                        nv.setNgayXuat(DateFormat.getDateInstance().format(datechooserngayxuat.getDate()));
                        nv.setMaKhachHang(cbbmakh.getSelectedItem().toString());
                        nv.setMaPhieuXuat(txtmahdpx.getText());
                        if (PhieuXuatdll.AddData(nv) == true) {
                            JOptionPane.showMessageDialog(this, "Thêm thành công!!!");
                            Biding_PhieuXuat();
                        } else {
                            JOptionPane.showMessageDialog(this, "Bạn đã nhập trùng mã phiếu nhập!!! ");
                            return;
                        }
                    } catch (Exception e) {

                    }

                }
            }

        } else {
            try {
                PhieuXuat nv = new PhieuXuat();
                nv.setMaNhanVien(txtmanhanvien.getText());
                nv.setNgayXuat(DateFormat.getDateInstance().format(datechooserngayxuat.getDate()));
                nv.setMaKhachHang(cbbmakh.getSelectedItem().toString());
                nv.setMaPhieuXuat(txtmahdpx.getText());
                PhieuXuatdll.updateData(nv);
                Biding_PhieuXuat();
                JOptionPane.showMessageDialog(this, "Sửa thành công!!!");
            } catch (Exception e) {

            }
        }
        LamMoi_tPhieuXuat();
    }//GEN-LAST:event_jButton19ActionPerformed
