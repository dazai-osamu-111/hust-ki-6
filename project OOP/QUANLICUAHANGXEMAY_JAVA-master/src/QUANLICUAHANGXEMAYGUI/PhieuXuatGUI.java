    private void jButton20ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton20ActionPerformed
        // TODO add your handling code here:
        them_PhieuXuat = false;
        txtmahdpx.setEnabled(false);
        if (row_table_PhieuXuat < 0) {
            JOptionPane.showMessageDialog(this, "Bạn chưa nhập dòng của bảng muốn sửa!!!");
            return;
        } else {
            txtmahdpx.setText(listPhieuXuat.get(row_table_PhieuXuat).getMaPhieuXuat());
            DateFormat formatter = new SimpleDateFormat("dd/MM/yyyy");
            try {

                Date date = formatter.parse(listPhieuXuat.get(row_table_PhieuXuat).getNgayXuat());
                datechooserngayxuat.setDate(date);
            } catch (ParseException ex) {
                Logger.getLogger(NhanVienGUI.class.getName()).log(Level.SEVERE, null, ex);
            }
            DefaultComboBoxModel model = new DefaultComboBoxModel();
            model.addElement(listPhieuXuat.get(row_table_PhieuXuat).getMaKhachHang().toString().trim());
            model.setSelectedItem(model.getElementAt(0));
            PhieuXuatGUI.cbbmakh.setModel(model);
            txtmahdpx.setEnabled(false);

        }
    }//GEN-LAST:event_jButton20ActionPerformed

